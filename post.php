                                <h2>D-Day 灾难来临日</h2>
                                <p>
                                    《D-Day》（灾难来临日）是韩国JTBC电视台于2015年9月18日起播出的金土迷你连续剧，到现在应该是已经完结的状态了，喜欢的朋友可以一次看个够，不用辛苦追剧了；这是韩国首部灾难型题材电视剧，主要讲述了首尔发生大地震后，灾难医疗小组成员冒着生命危险执行任务的医疗组故事；电视剧题材新颖，场景华丽，由金英光、郑素敏、金晶和、李成烈和尹珠熙等倾情出演，剧中将出现高楼大厦倒塌的画面，将运用CG技术展现壮观场面。
                                </p>
